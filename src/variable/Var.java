package variable;

public class Var {
    public  static  void main(String[] args) {
        int a;
        a = 20; //정수
        double b = 10.5; //실수
        boolean c = true; //참거짓
        char d = 'A'; //문자 하나
        String e = "황제"; //문자열
//1.
        System.out.println(10);
        System.out.println(10);
        System.out.println(10);

        System.out.println(20);
        System.out.println(20);
        System.out.println(20);
//2.
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
//3.
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        a = 20;
        System.out.println(a);
        a = 50;
        System.out.println(a);
    }
}

